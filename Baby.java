class Baby {
	double height;
	double width;
	int age;

	public Baby(int age, double height, double width) {
		this.age = age;
		this.width = width;
		this.height = height;
	}

	public int getAge() {
		return age;
	}

	public double getHeight() {
		return height;
	}

	public double getWidth() {
		return width;
	}

}